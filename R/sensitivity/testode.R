par <- c(2.3, 0.04, 0.55,
         5.1, 2.3, 1.7, 30, 12278210, 0,1000,1000,0,"constant",0)
init<-c(12278210-2270-34-0-745-0,2270,34,0,745,0)
t<-seq(0,1000,by=1)
pred=FALSE
simul1<-seirah_solve(init, t, par,pred)
simul2<- deSolve::ode(init, t, seirah_ode, par)
write.table(simul2,"./testcedric20200424.txt")
simul1<-as.data.frame(simul1)
simul2<-as.data.frame(simul2)
names(simul2)<-c("time","S","E","I","R","A","H")
names(simul1)<-c("time","S","E","I","R","A","H")
write.table(simul1,"./testcedric20200424_timebytime.txt")

par(mfrow=c(2,3))
plot(simul1$time,simul1$S/simul2$S,type="l")
plot(simul1$time,simul1$E/simul2$E,type="l")
plot(simul1$time,simul1$I/simul2$I,type="l")
